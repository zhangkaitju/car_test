package com.domain;

/**
 * 热点对象
 * @author zhangkai
 *
 */
public class HotPoint {
	private double lng;
	private double lat;
	private int count;
	
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
